    private void saveCustomerButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveCustomerButton1ActionPerformed
         if(!(NameText.getText().trim().equals(""))){
            int okayORnot = JOptionPane.showConfirmDialog(null,"Do you really want to ADD this ?","ADD",JOptionPane.YES_NO_OPTION);
        if(okayORnot==0){
        
                                int supID=Integer.parseInt(supIDText.getText());
				String first_Name=NameText.getText();
                                String discription=discriptionText.getText(); 
				String address=addressTextArea.getText();
                                String mobile_Number=mobileNumberText.getText();
                                String landLine=landLineText.getText();
                                String email=emailText.getText();
				
				Supplier supplier=new Supplier(supID, first_Name, discription, address, mobile_Number, landLine, email);
				try{
					int res=SupplierController.addSupplier(supplier);
					if(res>0){
						JOptionPane.showMessageDialog(Add_Supliers.this,"Added");
					}
				}catch(NumberFormatException | SQLException | ClassNotFoundException e){
                                JOptionPane.showMessageDialog(Add_Supliers.this,e.getMessage());
                                }
                                
                                supIDText.setText("");
                                NameText.setText("");
                                discriptionText.setText("");
                                addressTextArea.setText("");
                                mobileNumberText.setText("");
                                landLineText.setText("");
                                emailText.setText("");

                                try{
                                int ID =IDGenerator4Int.getNextId("supplier", "Supplier_ID");
                                supIDText.setText(Integer.toString(ID));
                                }catch (ClassNotFoundException | SQLException ex) {
                                    JOptionPane.showMessageDialog(Add_Supliers.this, ex);
                                }
                                
                                }
        }else{
        getToolkit().beep();
        JOptionPane.showMessageDialog(Add_Supliers.this,"First Name Can't Be Empty !");
        
        }
       

    }//GEN-LAST:event_saveCustomerButton1ActionPerformed

    private void ResetCustomerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ResetCustomerButtonActionPerformed

        if(!(NameText.getText().trim().equals(""))){

            supIDText.setText("");
            NameText.setText("");
            discriptionText.setText("");
            addressTextArea.setText("");
            mobileNumberText.setText("");
            landLineText.setText("");
            emailText.setText("");

            try{
            int ID =IDGenerator4Int.getNextId("supplier", "Supplier_ID");
            supIDText.setText(Integer.toString(ID));
            }catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(Add_Supliers.this, ex);
            }
        }else{
        getToolkit().beep();
        JOptionPane.showMessageDialog(Add_Supliers.this,"Nothing to reset !");
        
        }

    }//GEN-LAST:event_ResetCustomerButtonActionPerformed

