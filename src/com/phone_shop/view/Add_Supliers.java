    private void NameTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NameTextKeyTyped
        char c=evt.getKeyChar();

        if(!(Character.isLetter(c)||c==KeyEvent.VK_BACK_SPACE||c==KeyEvent.VK_SPACE||c==KeyEvent.VK_DELETE||c==KeyEvent.VK_ENTER)){
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_NameTextKeyTyped

