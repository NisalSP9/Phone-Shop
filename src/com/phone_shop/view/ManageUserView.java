    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        User user = new User(userNameText.getText(), privilageCB.getSelectedIndex(), passwordText.getText());
        UserControll userControll = new UserControll();
        try {
            int rst = userControll.addUser(user);

            if (rst > 0) {

                JOptionPane.showMessageDialog(ManageUserView.this, "Added...!");
                resetButton.doClick();

            } else {

                JOptionPane.showMessageDialog(ManageUserView.this, "Fail...!");

            }

        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(ManageUserView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_addButtonActionPerformed

