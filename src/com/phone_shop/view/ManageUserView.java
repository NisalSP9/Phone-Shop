    private void userNameTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_userNameTextKeyTyped
        char c = evt.getKeyChar();

        if (!(Character.isDigit(c) || Character.isLetter(c) || c == KeyEvent.VK_BACK_SPACE || c == KeyEvent.VK_DELETE || c == KeyEvent.VK_ENTER)) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_userNameTextKeyTyped

