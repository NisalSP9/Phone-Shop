    public void updateTableChequePayment(int offset){
        ChequeDetailController cashPayment = new ChequeDetailController();
        try {
            ResultSet rst = cashPayment.updateChequePaymentTable(offset);
            ViewTable.setModel(DbUtils.resultSetToTableModel(rst));             
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
        
    
    
    }
