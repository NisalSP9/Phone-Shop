    private void addressTextAreaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_addressTextAreaKeyTyped
        char c = evt.getKeyChar();

        if (!(Character.isLetter(c) || Character.isDigit(c) || c == KeyEvent.VK_SLASH || c == KeyEvent.VK_SPACE || c == KeyEvent.VK_COMMA || c == KeyEvent.VK_BACK_SPACE || c == KeyEvent.VK_DELETE || c == KeyEvent.VK_ENTER)) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_addressTextAreaKeyTyped

