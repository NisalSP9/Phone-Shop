    private void mobileNumberTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mobileNumberTextKeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c) || c == KeyEvent.VK_BACK_SPACE || c == KeyEvent.VK_DELETE || c == KeyEvent.VK_ENTER)) {
            getToolkit().beep();
            evt.consume();

        }
    }//GEN-LAST:event_mobileNumberTextKeyTyped

