    private void saveCustomerButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveCustomerButton1ActionPerformed

        if (!(NameText.getText().trim().equals("")) && !(addressTextArea.getText().trim().equals(""))) {

            int okayORnot = JOptionPane.showConfirmDialog(AddCustomerView.this, "Do you really want to ADD this ?", "ADD", JOptionPane.YES_NO_OPTION);
            if (okayORnot == 0) {

                int cust_ID = Integer.parseInt(cusIDText.getText());
                String Name = NameText.getText();

                String address = addressTextArea.getText();
                String mobile_Number = mobileNumberText.getText();
                String landLine = landLineText.getText();
                String email = emailText.getText();

                Customer customer = new Customer(cust_ID, Name, address, mobile_Number, landLine, email);
                try {
                    int res = CustomerController.addCustomer(customer);
                    if (res > 0) {
                        JOptionPane.showMessageDialog(AddCustomerView.this, "Added");
                        ResetCustomerButton.doClick();

                    }
                } catch (NumberFormatException | SQLException | ClassNotFoundException e) {
                    JOptionPane.showMessageDialog(AddCustomerView.this, e.getMessage());
                }
            }
        } else {
            getToolkit().beep();
            JOptionPane.showMessageDialog(AddCustomerView.this, "Name and Adress Can't Be Empty !");

        }


    }//GEN-LAST:event_saveCustomerButton1ActionPerformed

